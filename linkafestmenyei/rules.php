        <div class="paragraph" id="preparation">
          <h1>Preparation</h1>
          <p>Minimum amount of players: 5</p>
          <p>Maximum amount of players: 12</p>
          <p>Take places in a circle, if possible.
          Choose as many character cards as many players are given, then raffle the chosen cards
          and give everybody one (backside up). If you use this programme, after setting the characters,
          click "NEXT" and check the character card one by one, by passing the used device around.
          Make sure you "turned your card on its back" before passing it on.
          </p>
          <p>
          Now you are divided into two groups: The Minions of Mordred (the evils) and The Loyal Servants
          of Arthur. Some cards give you special abilities, which will be described <a href="#characters" style="color: rgb(226,154,6)">later</a>.
          </p>
        </div>
        <div class="paragraph" id="endofgame">
          <h1>End of Game</h1>
          <p>In this game players will be chosen for quests. A quest can end with VICTORY or DEFEAT.
          Since there is a maximum of 5 quests, minions of Mordred instantly win as 3 quests failed (ended width
          defeat). If 3 quests have succeeded, minions of Mordred have a last opportunity to win the game.
          The character "Assassin" (after discussing it with the other minions) guesses which player got
          the character card "Merlin". If guessing right, evil wins.
          </p>
        </div>
        <div class="paragraph" id="game">
          <h1>Game</h1>
          <p>
            The game consists of a maximum amount of 5 rounds. In these rounds players are chosen for quests
            and in the mean time they are discussing who are supposingly minions or servants of Arthur. One round
            lasts approximately 5 minutes, and the game makes no sense if one of the players doesn't take part in the
            discussion actively. The game starts with the "sleeping town", which is described <a style="color: rgb(226,154,6)" href="#characters">later</a> .
          </p>

            <h2 id="players">1.: Players are chosen for a quest</h2>
            <p>
            The one player on the turn selects players for the quest. The amount of players that are supposed
            to be selected, is shown on the table. This part of the game requires the longest discussion.
            The player on the turn can obviously select themselves as well. The chosen players will decide
            whether the round will end with success or with defeat, so they must be selected carefully.
          </p>

            <h2 id="vote">2.: General vote</h2>  <p>
            After players are named, all players (including the selected ones) vote. This vote defines if the
            chosen players will really be the ones going for the quest. If the vote has a negative result, new
            players are to be selected by the next player on the round (repeat step one). After a countdown, at the same time, all players
            vote width their hands, holding their thumb fingers up or down. The vote has a positive result if
            MORE than than 50% of the players vote for yes (50% + 1 vote required). If in the same round
            more than 4 times the vote ended with negative result, the 5th choice isn't to be voted. You can count
            how many times has the vote failed on the table (Vote Track).
          </p>

            <h2 id="quest">3.: Quest</h2><p>
            The players chosen for the quest and having been voted get 2 cards, a success and a fail card.
            If you use this programme, by passing the used device, you can select your "vote" by clicking on the propriate
            card. Servants of Arthur MUST vote with success! Minions of Mordred, however, can decide, whether they
            want the mission to fail or to succeed. This part goes secretely, after each player chose a card, they
            will be shown in a random order.
          </p>
            <h2 id="result">4.: Result</h2><p>
            This programme evaluates the result of the quest automatically. Only one "fail" card is enough for
            having the quest defeated. If there are more than 6 players (7 - 12), in the 4th round, two "fail" cards
            are required for the defeat.
          </p>
        </div><div class="paragraph" id="characters">
          <h1>Characters and sleeping town</h1>
          <p>
            Before the very first round comes the section "sleeping town". This way the characters with special abilities
            can get a bit more information.
            All players must close their eyes and put
            their hands in the middle. One player (keeping their eyes closed) leads this section, by announcing
            which characters can open their eyes, and which are supposed to sign with their fingers.
          </p>
          <p>
            The characters called Loyal Servant of Arthur, have no abilities at all
          </p>
          <p>
            The sleeping town starts with all minions of Mordred getting known eachother. In their section,
            all evil characters open their eyes and sign. The characters called: Minion of Mordred have no other
            abilities. <br>
            Note: Oberon stays out of this section
          </p>

            <h2 id="merlin">Merlin (Servant of Arthur)</h2><p>
            Merlin knows all the minions of Mordred. In his section, all minions of Mordred sign and Merlin opens
            his eyes.</br>
            <strong> Note: </strong>Merlin doesn't see Mordred and sees Sir Kay, who deceives him.
            </br>
            <strong> Strategy: </strong> He may lead the Servants of Arthur to the right path to win the game, but must do that silently
            because if Assassin figures out who he is, evil wins.
            </p>
            <h2 id="assassin">Assassin (Minion of Mordred)</h2><p>
            Assassin has no abilities, but at the end of game, if servants of Arthur would win the game, she guesses
            who Merlin is. If she's right, evil wins.</p>
            <h2 id="oberon">Oberon (Minion of Mordred)</h2><p>
            Oberon is character who cannot be seen by other evil character, and neither can he see them.
            In the section of evil characters, all evils sign with their hands, except Oberon, and all evils
            open their eyes, except Oberon.</p>
            <h2 id="percival">Percival (Loyal Servant of Arthur)</h2><p>
            Percival sees Merlin and an evil character called Morgana. Percival doesn't know accurately who Merlin is, he shall figure it out during the game.
            In his section Merlin and Morgana sign and Percival opens his eyes.
            <br><strong> Note: </strong> If Percival is chosen for a playing character, Morgana must be chosen as well!
            <br><strong> Strategy: </strong> As soon as Percival figured out, who Merlin is, should repeat every word that Merlin said,
            really loud, making Assassin believe he is Merlin.</p>
            <h2 id="morgana">Morgana (Minion of Mordred)</h2><p>
            Morgana is the character, that appears as Merlin to Percival. In Percival's section, Merlin and Morgana sign and Percival opens his eyes.
            <br>Strategy: Morgana should pretend to be Merlin to deceive Percival.</p>
            <h2 id="guinevere">Guinevere (Servant of Arthur)</h2><p>
            Guinevere sees all Loyal Servants of Arthur. In her section Loyal Servants of Arthur sign and Guinevere
            opens her eyes.
            <br><strong> Strategy: </strong> tries to appear as Merlin to Assassin.</p>
            <h2 id="mordred">Mordred</h2><p>
            Mordred is the character Merlin can't see. In Merlin's section, all evils sign, except Mordred, and Merlin
            opens his eyes.</p>
            <h2 id="sirkay">Sir Kay (Servant of Arthur)</h2><p>
            Sir Kay appears as evil to Merlin. In Merlin's section, all evils and Sir Kay sign and Merlin opens
            his eyes.
          </p>
